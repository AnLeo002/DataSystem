    private void actualizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarBtnActionPerformed
        String nombre = NombreTxt.getText().trim();
        String referencia = refeTxt.getText().trim();
        String cantidad = cantidadTxt.getText().trim();
        String marca = marcaTxt.getText().trim();
        long mili = System.currentTimeMillis();
        Date fecha = new Date(mili);

        if (nombre.equals("") || referencia.equals("") || cantidad.equals("") || marca.equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } else {
            try {
                Connection cn1 = Conexion.conectar();
                PreparedStatement pst1 = cn1.prepareStatement("update inventario set nombre=?,referencia=?,cantidad=?,marca=?,ultima_actualizacion=?,actualizado_por=? where id='" + id_inven + "'");

                pst1.setString(1, nombre);
                pst1.setString(2, referencia);
                pst1.setInt(3, Integer.parseInt(cantidad));
                pst1.setString(4, marca);
                pst1.setDate(5, fecha);
                pst1.setString(6, user);
                pst1.executeUpdate();
                cn1.close();

                JOptionPane.showMessageDialog(null, "Producto actualizado");

                this.dispose();

            } catch (SQLException e) {
                System.err.println("Existe un error de conexion con la base de datos " + e);
            }
        }
    }//GEN-LAST:event_actualizarBtnActionPerformed

