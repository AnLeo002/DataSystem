    public infoInventario() {
        initComponents();
        
        user= Login.user;
        id_inven = GestionarInventario.id_invetario;
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        setLocationRelativeTo(null);
        setResizable(false);
        

        ImageIcon fondoUrl = new ImageIcon("src/imagenes/wallpaperPrincipal.jpg");
        Icon fondo = new ImageIcon(fondoUrl.getImage().getScaledInstance(jLabelBack.getWidth(), jLabelBack.getHeight(), Image.SCALE_DEFAULT));
        jLabelBack.setIcon(fondo);
        repaint();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select tipo_nivel from usuarios where username = '" + user + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {

                tipo_nivel = rs.getString("tipo_nivel");

                if (tipo_nivel.equalsIgnoreCase("Administrador")) {
                    NombreTxt.setEditable(true);
                    refeTxt.setEditable(true);
                    cantidadTxt.setEditable(true);
                    marcaTxt.setEditable(true);                    
                } else {
                    NombreTxt.setEditable(false);
                    refeTxt.setEditable(false);
                    cantidadTxt.setEditable(false);
                    marcaTxt.setEditable(false);
                    eliminarBtn.setVisible(false);
                    actualizarBtn.setVisible(false);
                }

            }
        } catch (SQLException e) {
            System.err.println("Error en el boton Acceder: " + e);
            JOptionPane.showMessageDialog(null, "Eror al iniciar sesion!!, contacte al administrador.");
        }

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from inventario where id =  '" + id_inven + "'");

            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                this.setTitle("Producto "+rs.getString("nombre"));
                NombreTxt.setText(rs.getString("nombre"));
                refeTxt.setText(rs.getString("referencia"));
                cantidadTxt.setText(rs.getString("cantidad"));
                fechaRegistroTxt.setText(rs.getString("fecha_registro"));
                ultimaTxt.setText(rs.getString("ultima_actualizacion"));
                marcaTxt.setText(rs.getString("marca"));
                registradoTxt.setText(rs.getString("registrado_por"));
                actualizadoTxt.setText(rs.getString("actualizado_por"));

                cn.close();
            }
        } catch (SQLException e) {
            System.err.println("Error con la base de datos " + e);
        }
    }
    
