    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String nombre = NombreTxt.getText().trim();
        String email = emailTxt.getText().trim();
        String tel = telTxt.getText().trim();
        String user = usernameTxt.getText().trim();
        String pass = passwordTxt.getText().trim();
        if (nombre.equals("") || email.equals("") || tel.equals("") || user.equals("") || pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } else {
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst1 = cn.prepareStatement("select * from usuarios where username = '" + usernameTxt.getText().trim() + "'");

                ResultSet rs = pst1.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Nombre de usuario ya registrado");
                    cn.close();
                } else {
                    try {
                        cn.close();
                        
                        Connection cn2 = Conexion.conectar();
                        PreparedStatement pst = cn2.prepareStatement("insert into usuarios values(?,?,?,?,?,?,?,?,?)");

                        pst.setString(1, "0");
                        pst.setString(2, NombreTxt.getText().trim());
                        pst.setString(3, emailTxt.getText().trim());
                        pst.setString(4, telTxt.getText().trim());
                        pst.setString(5, usernameTxt.getText().trim());
                        pst.setString(6, passwordTxt.getText().trim());
                        pst.setString(7, statusTxt.getSelectedItem().toString().trim());
                        pst.setString(8, "Activo");
                        pst.setNString(9, userLog.trim());

                        pst.executeUpdate();
                        cn2.close();

                        JOptionPane.showMessageDialog(null, "Registro exitoso");

                        NombreTxt.setText("");
                        emailTxt.setText("");
                        telTxt.setText("");
                        usernameTxt.setText("");
                        passwordTxt.setText("");
                        //esta linea de condigo pone la primera opcion que se encuentra dentro del comboBox
                        statusTxt.setSelectedIndex(0);
                        
                        this.dispose();
                    } catch (SQLException e) {
                        System.err.println("Existe un problema de conexion con la base de datos: " + e);
                    }
                }
            } catch (SQLException e) {
                System.err.println("Problema de conexion con la base de datos "+ e);
            }

        }
    }//GEN-LAST:event_registerButtonActionPerformed

