            PreparedStatement pst = Conexion.buscarEquipo(buscar);
            ResultSet rs = pst.executeQuery();
            
            tablaEquipos = new JTable(model);

            jScrollPane1.setViewportView(tablaEquipos);

            model.addColumn("N de serie");
            model.addColumn("Tipo");
