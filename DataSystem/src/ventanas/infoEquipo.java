    private void actualizarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarEquipoActionPerformed
        String marca = marcaTxt.getSelectedItem().toString().trim();
        String tipo = tipoEqui.getSelectedItem().toString().trim();
        String modelo = modeloTxt.getText().trim();
        String serie = serieTxt.getText().trim();
        String obser = observacionesTxt.getText().trim();
        String status = statusTxt.getSelectedItem().toString().trim();
        String comen = comenTxt.getText().trim();

        if (modelo.equals("") || serie.equals("") || obser.equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        } else if (marca.equals("Seleccionar") || tipo.equals("Seleccionar")) {
            JOptionPane.showMessageDialog(null, "Seleccione una opción");
        } else {
            if (status.equals("Entrega") || status.equals("Reparado")) {
                if (JOptionPane.showConfirmDialog(null, "Si ya agrego las piezas usadas en este equipo, oprima 'yes' de lo contrario agregarlas") == 0) {
                    try {
                        Connection cn = Conexion.conectar();
                        if (tipo_nivel.equalsIgnoreCase("Tecnico")) {
                            PreparedStatement pst = cn.prepareStatement("update equipos set tipo_equipo=?, marca=?,modelo=?,num_serie=?,observaciones=?,estatus=?,comentarios_tecnicos=?,revision_tecnica=? where num_serie='" + num_serie + "'");
                            pst.setString(1, tipo);
                            pst.setString(2, marca);
                            pst.setString(3, modelo);
                            pst.setString(4, serie);
                            pst.setString(5, obser);
                            pst.setString(6, status);
                            pst.setString(7, comen);
                            pst.setString(8, Login.user);

                            pst.executeUpdate();
                            cn.close();
                        } else {
                            PreparedStatement pst = cn.prepareStatement("update equipos set tipo_equipo=?, marca=?,modelo=?,num_serie=?,observaciones=? where num_serie='" + num_serie + "'");
                            pst.setString(1, tipo);
                            pst.setString(2, marca);
                            pst.setString(3, modelo);
                            pst.setString(4, serie);
                            pst.setString(5, obser);

                            pst.executeUpdate();
                            cn.close();
                        }
                        JOptionPane.showMessageDialog(null, "Equipo actualizado correctamente");
                        this.dispose();

                    } catch (SQLException e) {
                        System.err.println("Existe un error de conexion con la base de datos " + e);
                    }
                }
            }

        }
    }//GEN-LAST:event_actualizarEquipoActionPerformed

