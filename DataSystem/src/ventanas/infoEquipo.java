    public infoEquipo() {
        initComponents();
        setLocationRelativeTo(null);
        setResizable(false);
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        setTitle("Información del equipo - Cliente " + infoCliente.nombre_cliente);

        ImageIcon fond = new ImageIcon("src/imagenes/wallpaperPrincipal.jpg");
        Icon fondo = new ImageIcon(fond.getImage().getScaledInstance(jLabelBack1.getWidth(), jLabelBack1.getHeight(), Image.SCALE_DEFAULT));
        jLabelBack1.setIcon(fondo);
        repaint();

        nombreTxt.setText(infoCliente.nombre_cliente);

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select tipo_nivel from usuarios where username = '" + user + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {

                tipo_nivel = rs.getString("tipo_nivel");

                if (tipo_nivel.equalsIgnoreCase("Administrador") || tipo_nivel.equalsIgnoreCase("Tecnico")) {
                    statusTxt.setEditable(true);
                    comenTxt.setEditable(true);
                    num_serie = GestionarEquipos.num_serie;
                } else {
                    statusTxt.setEditable(false);
                    comenTxt.setEditable(false);
                    num_serie = infoCliente.num_serie;
                }

            }
        } catch (SQLException e) {
            System.err.println("Error en el boton Acceder: " + e);
            JOptionPane.showMessageDialog(null, "Eror al iniciar sesion!!, contacte al administrador.");
        }

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from equipos where  num_serie='" + num_serie + "'");

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                tipoEqui.setSelectedItem(rs.getString("tipo_equipo"));
                marcaTxt.setSelectedItem(rs.getString("marca"));
                modeloTxt.setText(rs.getString("modelo"));
                serieTxt.setText(rs.getString("num_serie"));
                observacionesTxt.setText(rs.getString("observaciones"));
                comenTxt.setText(rs.getString("comentarios_tecnicos"));
                statusTxt.setSelectedItem(rs.getString("estatus"));
                fechaTxt.setText(rs.getString("fecha_ingreso"));
                tecnicoTxt.setText(rs.getString("revision_tecnica"));
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error de conexion con la tabla equipo " + e);
        }
    }

