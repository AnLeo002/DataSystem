    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        model.setColumnCount(0);
        model.setRowCount(0);

        String buscar = buscarTxt.getSelectedItem().toString().trim();
        String opcion = opcionTxt.getText().trim();
        String query = null;

        if ((buscar.equals("Numero de Serie") || buscar.equals("Producto") || buscar.equals("Fecha")) && opcion.equals("")) {
            JOptionPane.showMessageDialog(null, "Complete los campos para generar la busqueda");
        } else {
            try {

                if (buscar.equals("Numero de Serie")) {
                    query = "select id,id_inventario,serie_equipo,cantidad,nombre_inventario,fecha_uso from inventario_usado where serie_equipo = '" + opcion + "'";
                } else if (buscar.equals("Producto")) {
                    query = "select id,id_inventario,serie_equipo,cantidad,nombre_inventario,fecha_uso from inventario_usado where nombre_inventario = '" + opcion + "'";
                }else if (buscar.equals("Todos")) {
                    query = "select id,id_inventario,serie_equipo,cantidad,nombre_inventario,fecha_uso from inventario_usado";
                }

                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(query); 
                ResultSet rs = pst.executeQuery();

                tablaEquipos = new JTable(model);

                jScrollPane1.setViewportView(tablaEquipos);

                model.addColumn("ID");
            model.addColumn("ID inventario");
            model.addColumn("N serie");
            model.addColumn("Cantidad");
            model.addColumn("Inventario");
            model.addColumn("Fecha");
                if (rs.next()) {
                    do {

                        Object[] fila = new Object[6];
                        for (int i = 0; i < 6; i++) {
                            fila[i] = rs.getObject(i + 1);
                        }                        
                        model.addRow(fila);
                    }while (rs.next());
                }else{
                        JOptionPane.showMessageDialog(null, "Opcion no encontrada. Por favor vuelva a intentar");
                }
                
            } catch (SQLException e) {
                System.err.println("Existe un error de conexion con la base de datos " + e);
            }
        }


    }//GEN-LAST:event_buscarBtnActionPerformed

