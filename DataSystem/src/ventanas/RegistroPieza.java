    private void agregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBtnActionPerformed
        String nombre = NombreTxt.getText().trim();
        String refer = referTxt.getText().trim();
        String cantidad = cantidadTxt.getText().trim();
        long mili = System.currentTimeMillis();
        Date fecha = new Date(mili);

        if (nombre.equals("") || cantidad.equals("") || cantidad.equals("")) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }else{
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst1 = cn.prepareStatement("select id,cantidad,nombre from inventario where referencia = '"+refer+"'");
                ResultSet rs = pst1.executeQuery();
                if (rs.next()) {
                    id_inve = rs.getInt(1);
                    cantidad_inventario=rs.getInt(2); 
                    
                    if(cantidad_inventario<Integer.parseInt(cantidad)){
                        JOptionPane.showMessageDialog(null, "La cantidad en el invetario es menor, por favor revisar.");
                    }else{
                        try {                        
                        Connection cn2 = Conexion.conectar();
                        PreparedStatement pst = cn2.prepareStatement("insert into inventario_usado values(?,?,?,?,?,?)");
                       
                        pst.setString(1, "0");
                        pst.setInt(2, id_inve);
                        pst.setString(3, num_serie);
                        pst.setInt(4,Integer.parseInt(cantidad) );
                        pst.setString(5, rs.getString(3));
                        pst.setDate(6, fecha);                       
                        pst.executeUpdate();
                        cn2.close();

                        JOptionPane.showMessageDialog(null, "Registro exitoso");

                        this.dispose();
                        
                            try {
                                Connection cn3 = Conexion.conectar();
                                resultado = cantidad_inventario-Integer.parseInt(cantidad);
                                PreparedStatement pst3 = cn3.prepareStatement("update inventario set cantidad=? where id ='"+id_inve+"'");
                                pst3.setInt(1, resultado);
                                pst3.executeLargeUpdate();
                                cn3.close();
                            } catch (SQLException e) {
                                System.err.println("Error al actualizar la cantidad de inventario: "+e);
                            }
                    } catch (SQLException e) {
                        System.err.println("Existe un problema de conexion con la base de datos: " + e);
                    }
                    }
                    cn.close();
                } else {
                    JOptionPane.showMessageDialog(null, "Referencia no registrada, por favor intente con otra referencia");
                    
                }
            } catch (SQLException e) {
                System.err.println("Problema de conexion con la base de datos "+ e);
            }

        }
    }//GEN-LAST:event_agregarBtnActionPerformed

