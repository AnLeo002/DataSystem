    public GestionarClientes() {
        initComponents();
        
        user = Login.user;
        setLocationRelativeTo(null);
        setResizable(false);
        setTitle("Capturista- Sesion de "+user);
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon url = new ImageIcon("src/imagenes/wallpaperPrincipal.jpg");
        Icon fondo = new ImageIcon(url.getImage().getScaledInstance(jLabelBack.getWidth(), jLabelBack.getHeight(), Image.SCALE_DEFAULT));
        jLabelBack.setIcon(fondo);
        repaint();
        
        DefaultTableModel model = new DefaultTableModel();
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select id_cliente,nombre_cliente,email_cliente,tel_cliente,ultima_modificacion from clientes ");
            
            ResultSet rs = pst.executeQuery();
            
            tablaClientes = new JTable(model);
            
            jScrollPane1.setViewportView(tablaClientes);
            
            model.addColumn("id");
            model.addColumn("Nombre");
            model.addColumn("Email");
            model.addColumn("Telefono");
            model.addColumn("Registrado por");
            
            while (rs.next()) {
                
                Object [] fila = new Object[5];
                for (int i = 0; i < 5; i++) {
                    fila[i]=rs.getObject(i+1);
                    
                }
                model.addRow(fila);
            }
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error de conexion con la base de datos "+e);
        }
        tablaClientes.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent e){
                int fila_point = tablaClientes.rowAtPoint(e.getPoint());
                int column_point = 0;
                
                if (fila_point>-1) {
                    id=(int)model.getValueAt(fila_point, column_point);
                    new infoCliente().setVisible(true);
                }
            }
        });
    }

